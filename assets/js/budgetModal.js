/**
 * Abre o cierra el modal para agregar el presupuesto
 */
function toggleAddBudgetModal(){
    document.getElementById('addBudget').classList.toggle('hidden');
}


/**
 * Abre o cierra el modal para modificar el presupuesto
 */
function toggleEditBudgetModal(){
    document.getElementById('editBudget').classList.toggle('hidden');
}


